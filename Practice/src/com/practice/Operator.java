package com.practice;

public class Operator {
public static void main( String[] args ) {
	int a=10;
	int b=20;
	System.out.println(a+b);
	System.out.println(a-b);
	System.out.println(a*b);
	System.out.println(a/b);
	System.out.println(a%b);
	System.out.println(a++);
	System.out.println(a);
	System.out.println(++a);
	System.out.println(a);
	System.out.println(a--);
	System.out.println(a);
	System.out.println(--a);
	System.out.println(a);
	System.out.println(a==b);
	System.out.println(a!=b);
	System.out.println(a>b);
	System.out.println(a<b);
	System.out.println(a<=b);
	System.out.println(a>=b);
	System.out.println(a<b && a!=b);
	System.out.println(a>b && a==b);
	System.out.println(a<=b && a==b);
	System.out.println(a>=b && a!=b);
	System.out.println(a<b || a==b);
	System.out.println(a>b || a!=b);
	int c = 40;
	 c -=4;
	 c +=3;
	System.out.println(c);
	
	
}
}
